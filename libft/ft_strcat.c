#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*temp;
	int		i;
	int		j;

	i = 0;
	j = 0;
	temp = s1;
	while (s1[i])
		i++;
	while (s2[j])
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
